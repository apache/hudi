package org.apache.hudi.io;

import org.apache.hudi.common.model.HoodieRecord;
import org.apache.hudi.common.util.Option;

import java.io.Closeable;
import java.util.Map;

/**
 * Writes CDC records generated by merge handles.
 */
public interface HoodieCDCLogWriter<T> extends Closeable {

  default void put(HoodieRecord hoodieRecord, T oldRecord, Option<T> newRecord) {
    put(hoodieRecord.getRecordKey(), oldRecord, newRecord);
  }

  void put(String recordKey, T oldRecord, Option<T> newRecord);

  void remove(String recordKey);

  Map<String, Long> getCDCWriteStats();

  @Override
  void close();
}
